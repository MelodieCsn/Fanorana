
public class Pion {
	private int x;
	private int y;
	private boolean couleur; //false=blanc et true=noir 
	private boolean danger;
	
	
	
	public int getX (){
		return x;
	}
	public void setX(int x){
		this.x = x;
	}
	public int getY (){
		return y;
	}
	public void setY(int y){
		this.y = y;
	}
	public boolean getCouleur (){
		return couleur;
	}
	public void setCouleur(boolean c){
		couleur = c;
	}
	public boolean getDanger(){
		return danger;
	}
	public void setDanger(boolean d){
		danger = d;
	}
	
	public Pion(){
		x=0;
		y=0;
		couleur=false;
		danger=false;
		
	}
	public Pion(int x,int y,boolean couleur, boolean danger){
		this.x = x;
		this.y = y;
		this.couleur = couleur;
		this.danger = danger;
	}
	
}
